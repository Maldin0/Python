def seq(num):
    '''Sequence XI'''
    for i in range(-num+1, num):
        for j in range(-num+1, num):
            if i == 0 or j == 0:
                print("%02d" %(num-max(abs(i), abs(j))), end=" ")
            elif abs(i) == abs(j):
                print("%02d" %num, end=' ')
            elif abs(abs(i)-abs(j)) < num:
                print("%02d" %(num-abs(abs(i)-abs(j))), end=" ")
        print()
seq(int(input()))
